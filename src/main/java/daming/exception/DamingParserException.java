package daming.exception;

/**
 * Represents a parser exception that occurred in <code>Daming</code>.
 *
 * @author Terence Chong Guang Jun
 */
public class DamingParserException extends DamingException {
    /**
     * Creates a new <code>DamingParserException</code> with the given message.
     *
     * @param message the exception string.
     */
    public DamingParserException(String message) {
        super(message);
    }
}
