package duke.filter;

/**
 * Comparison Operators used in filter.
 */
public enum ComparisonOperator {
    /**
     * Less than.
     */
    LessThan,
    /**
     * Less than or equal to.
     */
    LessThanOrEqualTo,
    /**
     * Equal to.
     */
    EqualTo,
    /**
     * Greater than or equal to.
     */
    GreaterThanOrEqualTo,
    /**
     * Greater than.
     */
    GreaterThan;
}
