package myduke.type;

public enum MessageFormatType {

    /** Print message with indentation but no boundary.  */
    MESSAGE_FORMAT_NO_BOUNDARY_WITH_INDENT,

    /** Print message without indentation and boundary.  */
    MESSAGE_FORMAT_NO_BOUNDARY_WITHOUT_INDENT,

    /** Print message with indentation and boundary. */
    MESSAGE_FORMAT_WITH_BOUNDARY_AND_INDENT
}
